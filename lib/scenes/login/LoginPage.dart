  void getSessionActive() async {
    currentUser = await _auth.currentUser();
    if(currentUser != null) {
      loginEmailController.text = currentUser.email;
    }
  }

  bool _isValid() {
    if (loginEmailController.text.isEmpty || loginEmailController.text == "") {
      showInSnackBar("Opps.. falta el correo");
      return false;
    }

    if (loginPasswordController.text.isEmpty || loginPasswordController.text == "") {
      showInSnackBar("Opps.. Falta la contraseña");
      return false;
    }
    return true;
  }

  bool _isValidRegister() {
    bool emailValid = RegExp(r"^[a-zA-Z0-9.]+@[a-zA-Z0-9]+\.[a-zA-Z]+").hasMatch(signupEmailController.text);

    if (signupNameController.text.isEmpty || signupNameController.text == "") {
      showInSnackBar("Opps.. Falta el nombre");
      return false;
    }
    if (signupEmailController.text.isEmpty || signupEmailController.text == "") {
      showInSnackBar("Opps.. falta el correo");
      return false;
    }

    if(!emailValid) {
      showInSnackBar("Opps.. Correo no valido");
      return false;
    }

    if (signupConfirmPasswordController.text.isEmpty || signupConfirmPasswordController.text == "") {
      showInSnackBar("Opps.. Falta la contraseña");
      return false;
    }

    if( signupPasswordController.text.length < 8){
      showInSnackBar("*La contraseña < 8 caracteres, numeros y letras");
      return false;
    }

    if (signupConfirmPasswordController.text != signupPasswordController.text) {
      showInSnackBar("Opps.. las contraseñas no coinciden");
      return false;
    }


    return true;
  }


  void _loginAtenpiData() async {
    if( _isValid() ) {
      await _auth.signInWithEmailAndPassword(
        email: loginEmailController.text,
        password: loginPasswordController.text,
      ).catchError((e) {
        if (e.code == "ERROR_USER_NOT_FOUND") {
          showInSnackBar("Usuario no registrado");
        }else if (e.code == "ERROR_WRONG_PASSWORD"){
          showInSnackBar("Usuarion o Contraseña incorrecta");
        } else {
          showInSnackBar("!Algo salio mal! \n Intenta nuevamente ");
        }
        print(e);
      }).then((user) {
        if (user != null) {
          Navigator.pushReplacement(context,  MaterialPageRoute(builder: (context) => NavigationdPage()));
        }
      });
    }
  }

  void _loginAtenppiAnonimous() async {
    await _auth
        .signInAnonymously()
        .then((user){
      Navigator.pushReplacement(context,  MaterialPageRoute(builder: (context) => NavigationdPage()));
    }
    );
  }

  void _registerAttempAcount() async {
    if (_isValidRegister()) {

      await _auth.createUserWithEmailAndPassword(
          email: signupEmailController.text,
          password: signupConfirmPasswordController.text)
          .then((user){
        if (user != null) {
          User userData = Mappers.getUserContext(user.user);
          Navigator.push(context,  MaterialPageRoute(builder: (context) => RegisterPage(userData)));
        }
      }).catchError((e){
        if(e.code == "ERROR_EMAIL_ALREADY_IN_USE") {
          showInSnackBar("Opps.. estos datos ya estan en uso");
        } else if(e.code == "ERROR_INVALID_EMAIL") {
          showInSnackBar("Opps.. Correo no valido");
        }
      });
    }
  }


